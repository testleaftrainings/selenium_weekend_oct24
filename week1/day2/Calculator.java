package week1.day2;

public class Calculator {

	
	//add
	public float add(int a, float b) {
		float c =a+b;
		return c;
	}
	//sub
	public int sub(int a, int b) {
		int c = a-b;
		return c;
	}
	//multiple
	public float multiple(float a , float b) {
		float c =a*b;
		return c;
	}
	//div
	
	public float div(int a, int b) {
		float c = a/b;
		return c;
	}
	
	
		
}
